// Define a function named last that accepts one argument, array
//   Declare a variable i and assign it to the result of the expression, length of array - 1
//   Declare a variable lastElement and assign it to the array at index i
//   Return the value of lastElement variable
function last(array) {
  var i = array.length - 1;
  var lastElement = array[i];
  return lastElement;
}
